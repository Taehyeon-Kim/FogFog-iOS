import UIKit.UIColor

extension UIColor {
    
    @nonobjc class var fogBlue: UIColor {
        return UIColor(red: 91/255, green: 157/255, blue: 255/255, alpha: 1.0)
    }
    
    @nonobjc class var fogBlack: UIColor {
        return UIColor(red: 21/255, green: 22/255, blue: 24/255.0, alpha: 1.0)
    }
    
    @nonobjc class var gray1: UIColor {
        return UIColor(red: 68/255, green: 69/255, blue: 71/255.0, alpha: 1.0)
    }
    
    @nonobjc class var shadowGray: UIColor {
        return UIColor(red: 28/255, green: 76/255, blue: 200/255.0, alpha: 1.0)
    }
    
    @nonobjc class var kakaoYellow: UIColor {
        return UIColor(red: 255/255, green: 219/255, blue: 30/255.0, alpha: 1.0)
    }
}

extension UIColor {
    @nonobjc class var blue8: UIColor {
